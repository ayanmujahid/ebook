<script>
    document.addEventListener("DOMContentLoaded", function() {
        let pageUrl = window.location.href; // Get the current page URL

        document.querySelectorAll("a, button, .owl-dot").forEach(element => {
            let text = element.textContent.trim(); // Get the visible text
            let ariaLabel = text; // Default to the element's text

            if (element.classList.contains("owl-prev")) {
                ariaLabel = "Previous Slide";
            } else if (element.classList.contains("owl-next")) {
                ariaLabel = "Next Slide";
            } else if (element.classList.contains("owl-dot")) {
                ariaLabel = "Carousel Dot";
            } else if (element.tagName.toLowerCase() === "a") {
                let href = element.getAttribute("href");

                if (!href || href === "javascript:" || href === "javascript:;" || href === "#") {
                    element.setAttribute("href", "#"); // Ensure there's always a valid href

                    // Use text as aria-label if available; otherwise, fallback to page URL
                    ariaLabel = text || pageUrl;
                } else {
                    ariaLabel = href; // Use the link as the aria-label if it's valid
                }
            } else if (element.tagName.toLowerCase() === "button") {
                // If it's a button but has no meaningful text, set a default description
                ariaLabel = text || "Button";
            }

            // Ensure aria-label is not empty and descriptive
            if (!ariaLabel || ariaLabel.trim() === "") {
                if (element.tagName.toLowerCase() === "button") {
                    ariaLabel = "Button";
                } else if (element.tagName.toLowerCase() === "a") {
                    ariaLabel = "Link";
                } else {
                    ariaLabel = "Interactive Element";
                }
            }

            element.setAttribute("aria-label", ariaLabel);
        });
    });
</script>
