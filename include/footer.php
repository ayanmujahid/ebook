    function setImgAttributes(img) {
        var imageUrl = img.getAttribute('src') || img.getAttribute('data-src');
        if (imageUrl) {
            var fileName = imageUrl.split('index.php').pop().split('.')[0];
            img.alt = fileName;
            img.loading = 'eager';

            // Function to update width and height based on intrinsic size
            function updateSize() {
                setTimeout(() => {
                    img.removeAttribute("width");
                    img.removeAttribute("height");
                    requestAnimationFrame(() => {
                        img.setAttribute("width", img.naturalWidth);
                        img.setAttribute("height", img.naturalHeight);
                    });
                }, 10); // Small delay to allow reflow
            }

            // Ensure update after image loads
            img.onload = updateSize;

            // If the image is already loaded, update size immediately
            if (img.complete) {
                updateSize();
            }

            // Listen for resize and orientation changes
            window.addEventListener("resize", updateSize);
            window.addEventListener("orientationchange", updateSize);
        } else {
            console.error('Image URL not found for element:', img);
        }
    }

    function applyImgAttributes() {
        document.querySelectorAll("img").forEach(setImgAttributes);
    }

    // Apply attributes on page load
    window.addEventListener("load", applyImgAttributes);
    document.addEventListener("DOMContentLoaded", applyImgAttributes);
</script>

